lst=[3,1,6,8,4,2,7,9,6,3]
print(lst)
print(sorted(lst)) #returns the sorted order
print(lst)
lst.sort() #sorts the list and returns None
print(lst)
print(lst.count(6)) #returns the number of occurances of the given element in the list
lst.reverse() #reverses the list and returns None
print(lst)
lst.clear() #clears the list and returns None
print(lst)
